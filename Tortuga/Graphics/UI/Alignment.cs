namespace Tortuga.Graphics.UI
{
    /// <summary>
    /// Type of horizontal alignment for the ui element
    /// </summary>
    public enum UiHorizontalAlignment
    {
        /// <summary>
        /// Align ui to left
        /// </summary>
        Left,
        /// <summary>
        /// Align ui to center
        /// </summary>
        Center,
        /// <summary>
        /// Align ui to right
        /// </summary>
        Right
    }

    /// <summary>
    /// Type of vertical alignment for the ui element
    /// </summary>
    public enum UiVerticalAlignment
    {
        /// <summary>
        /// Align ui to top
        /// </summary>
        Top,
        /// <summary>
        /// Align ui to center
        /// </summary>
        Center,
        /// <summary>
        /// Align ui to bottom
        /// </summary>
        Bottom
    }
}